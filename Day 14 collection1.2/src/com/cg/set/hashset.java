package com.cg.set;
import java.util.HashSet;
import java.util.Set;

public class hashset {

	public static void main(String[] args) {
//		hashset is an unordered set
		Set<Object>obj=new HashSet<Object>();
		obj.add("Bhakti");
		obj.add("e");
		obj.add(2.4);
		obj.add(9.8f);
		obj.add(12);
		System.out.println(obj);
		
		

	}

}
